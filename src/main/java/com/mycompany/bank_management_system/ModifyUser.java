    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String UserID = userId.getText();
        String Name = name.getText();
        String dob = ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText();
        String gender = "";
        if(male.isSelected()){
           gender = "Male";
        }else if(female.isSelected()){
          gender = "Female";
        }
        
        String Cnic = cnic.getText();
        String Address = address.getText();
        String account = "";
        if(current.isSelected()){
         account = "Current";
         
        }else if(saving.isSelected()){
         account = "Saving";
        }
        try{
         if(Name.equals("") || dob.equals("") || gender.equals("") || Address.equals("") || Cnic.equals("") || account.equals("")){
         JOptionPane.showMessageDialog(null, "Please Fill All Fields*");
         }
         else{
     
          mysqlConnection c = new mysqlConnection();
          String query = "Update Register SET UserName=? ,DOB = ? ,Gender = ?,CNIC = ? ,Address = ?,AccountType = ? Where Id = '"+UserID+"' ";
          PreparedStatement ps = c.conn.prepareStatement(query);  // c is object of mysqlConnection class and conn is connection of database.
          ps.setString(1, Name);
          ps.setString(2, dob);
          ps.setString(3, gender);
          ps.setString(4, Cnic);
          ps.setString(5, Address);
          ps.setString(6, account);
  
          ps.executeUpdate();
          JOptionPane.showMessageDialog(null, "User Updated Successfully");
    
          
         }
        }catch(Exception ex){
        System.out.println(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      String UserID = userId.getText();
      try{
       mysqlConnection c = new mysqlConnection();
          String query = "Select * FROM Register WHERE Id = '"+UserID+"' " ;
          ResultSet rs = c.s.executeQuery(query);   // For DDL use executeQuery. ResultSet store data which return data from database.
          while(rs.next()){
              name.setText(rs.getString("UserName"));
              cnic.setText(rs.getString("CNIC"));
              address.setText(rs.getString("Address"));
          }
      }catch(Exception ex){
        System.out.println(ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

