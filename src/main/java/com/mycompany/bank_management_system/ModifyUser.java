    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String UserID = userId.getText();
        try{
          if(UserID.equals("")){
             JOptionPane.showMessageDialog(null, "Please Enter Amount to Deposit or PIN Number*");
          }else{
          
          mysqlConnection c = new mysqlConnection();

          String query  = "Delete FROM Register where Id = '"+UserID+"' ";
          
          PreparedStatement ps = c.conn.prepareStatement(query);       // c is object of mysqlConnection class and conn is connection of database.
          ps.executeUpdate();
          JOptionPane.showMessageDialog(null, "User Deleted successfully");
          userId.setText(null);
          }
        }catch(Exception ex){
        System.out.println(ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

